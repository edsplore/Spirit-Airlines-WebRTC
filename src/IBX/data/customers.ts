// Function to get a random customer based on gender and behavior
export function getRandomCustomer(
  gender: "Male" | "Female",
  behavior: string
): CustomerDetails {
  // Filter customers by gender
  const filteredCustomers = customerList.filter(
    (customer) => customer.gender === gender
  );

  // If no matching customers, return a default one
  if (filteredCustomers.length === 0) {
    return customerList[0];
  }

  // Get a random customer from the filtered list
  const randomIndex = Math.floor(Math.random() * filteredCustomers.length);

  // Add behavior to the customer object
  return {
    ...filteredCustomers[randomIndex],
    behavior,
  };
}
